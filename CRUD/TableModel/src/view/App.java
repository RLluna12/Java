    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
        Cliente p = new Cliente();
        
        p.setCorte(txtCorte.getText());
        p.setNome(txtNome.getText());
        p.setValor(Double.parseDouble(txtValor.getText()));
        JOptionPane.showMessageDialog(null, "Adicionado");
        tableModel.addRow(p);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Digitou algo errado");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

     private void limparCampos()
    {
        txtNome.setText("");
        txtCorte.setText("");
        txtValor.setText("");
    }
     
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        if(jTProdutos.getSelectedRow()!= -1){
            tableModel.removeRow(jTProdutos.getSelectedRow());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
       
        if(jTProdutos.getSelectedRow()!= -1){
            tableModel.setValueAt(txtNome.getText(), jTProdutos.getSelectedRow(),0 );
            tableModel.setValueAt(txtCorte.getText(), jTProdutos.getSelectedRow(),1 );
            tableModel.setValueAt(txtValor.getText(), jTProdutos.getSelectedRow(),2 );
        }
        
       
    }//GEN-LAST:event_jButton3ActionPerformed

